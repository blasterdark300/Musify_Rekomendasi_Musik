        before = cleaned_data.shape[0]
        cleaned_data = cleaned_data[(cleaned_data[feature] >= lower_bound) & (cleaned_data[feature] <= upper_bound)]
        after = cleaned_data.shape[0]
        print(f"{feature}: Removed {before - after} outliers")
